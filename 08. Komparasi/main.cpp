#include <iostream>

int main() {

    int a = 2;
    int b = 2;

    // sama dengan
    std::cout << (a==b) << std::endl;

    // tidak sama dengan
    std::cout << (a!=b) << std::endl;

    // lebih dari
    std::cout << (a>b) << std::endl;

    // kurang dari
    std::cout << (a<b) << std::endl;

    // lebih dari atau sama dengan
    std::cout << (a>=b) << std::endl;

    // kurang dari atau sama dengan
    std::cout << (a<=b) << std::endl;

    return 0;
}

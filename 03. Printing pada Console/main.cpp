#include <iostream>

int main() {
    // satu baris
    std::cout << "Hello, World!"<< std::endl;

    // dua baris
    std::cout << "Hello, World!"<< std::endl;
    std::cout << "Hello, Unhas!"<< std::endl;

    std::cout << "Hello, World!\n";
    std::cout << "Hello, Unhas!"<< std::endl;

    // tergabung
    std::cout << "Hello, World!";
    std::cout << "Hello, Unhas!"<< std::endl;
    return 0;
}

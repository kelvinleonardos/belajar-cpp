#include <iostream>

int main() {

    int a = 7;
    int b = 4;

    int hasil;

    // penjumlahan
    hasil = a + b;
    std::cout << hasil << std::endl;

    // pengurangan
    hasil = a - b;
    std::cout << hasil << std::endl;

    // perkalian
    hasil = a * b;
    std::cout << hasil << std::endl;

    // pembagian
    hasil = a / b;
    std::cout << hasil << std::endl;

    // modulus
    hasil = a % b;
    std::cout << hasil << std::endl;

    return 0;
}
